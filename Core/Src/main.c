/* USER CODE BEGIN 4 */
void maybeBlink(void)
{
  static uint32_t lastTick = 0;
  static uint8_t ledOn = 0;

  if (blinks > 0)
  {
    uint32_t now = HAL_GetTick();

    if ((now - lastTick) >= 300)   // 300 ms elapsed
    {
      HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
      lastTick = now;
      ledOn = !ledOn;

      if (ledOn == 0)  // LED just turned off -> completed one blink cycle
      {
        blinks--;
      }
    }
  } else {
    HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
  }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART2)
  {
    if (rxData == 's')   // Stop command
    {
      blinks = 0;

      char msg[] = "Stopped blinking\r\n";
      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);

      rxIndex = 0;   // reset buffer (optional, in case something was half-typed)
    }
    else if (rxData == '\n' || rxData == '\r')   // End of line
    {
      // Add terminator
      rxBuffer[rxIndex] = 0;

      int value = atoi((char*)rxBuffer);
      if (value > 0)
      {
        blinks = value;

        char msg[50];
        sprintf(msg, "Blinking %d times...\r\n", blinks);
        HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
      }
      else
      {
        char msg[] = "Invalid number\r\n";
        HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
      }

      rxIndex = 0;   // reset buffer

      // Echo back
//      HAL_UART_Transmit(&huart2, rxBuffer, rxIndex, HAL_MAX_DELAY);
//      HAL_UART_Transmit(&huart2, (uint8_t*)"\r\n", 2, HAL_MAX_DELAY);

      rxIndex = 0;   // reset buffer
    }
    else
    {
      if (rxIndex < RX_BUFFER_SIZE - 1)
      {
        rxBuffer[rxIndex++] = rxData;
      }
    }

    // Restart interrupt reception for next byte
    HAL_UART_Receive_IT(&huart2, &rxData, 1);
  }
}
